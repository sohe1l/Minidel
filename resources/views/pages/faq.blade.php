<h3>FAQ: Frequently Asked Questions</h3>


<h4>What is Minidel?</h4>
<p>Minidel, short for Mini Deliveries, is an online ordering platform that focuses mainly on ordering from nearby stores that are offering no minimum delivery amount and no delivery charge.</p>


<h4>What can I order on Minidel?</h4>
<p>At the moment we have partnered with many restaurants and cafes across JLT, Dubai. Our goal is to cover any deliverable item such as food, groceries, medicene and grocories.</p>

<h4>Do you handle the delivery also?</h4>
<p>No. we are an online platform that make placing orders easier for the customers and also making it easier for the stores to manage their orders.</p>

<h4>Where is Minidel from?</h4>
<p>We lisenced and operating from Emirate of Ajman in United Arab Emirates.</p>

<h4>Are there any extra charges for placing orders on Minidel?</h4>
<p>We are not adding any extra charge for ordering. All the prices and delivery charges (if any) are set by individial stores.</p>

